SELECT SUM(TITENS_PDV.VLR_LIQ * TITENS_PDV.QTDE) TOTAL_VALOR_BRUTO_PDV_NUMBER
  FROM TITENS_PDV TITENS_PDV,
       TITENS_COMERCIAL TITENS_COMERCIAL,
       TREPRESENTANTES TREPRESENTANTES,
       TITENS_EMPR TITENS_EMPR,
       TITENS TITENS,
       TTIPOS_NF TTIPOS_NF,
       TDIVISOES_VENDAS TDIVISOES_VENDAS,
       TPEDIDOS_VENDA TPEDIDOS_VENDA,
       TCLIENTES TCLIENTES
 WHERE TITENS_COMERCIAL.ID = TITENS_PDV.ITCM_ID
   AND TREPRESENTANTES.ID = TPEDIDOS_VENDA.REP_ID
   AND TITENS_EMPR.ID = TITENS_COMERCIAL.ITEMPR_ID
   AND TITENS.ID = TITENS_EMPR.ITEM_ID
   AND TTIPOS_NF.ID = TITENS_PDV.TPNF_ID
   AND TDIVISOES_VENDAS.ID = TITENS_PDV.DIVD_ID
   AND TPEDIDOS_VENDA.ID = TITENS_PDV.PDV_ID
   AND TCLIENTES.ID = TPEDIDOS_VENDA.CLI_ID
   AND TITENS_PDV.DT_GERACAO between :DTINI and :DTFIM
   AND (TTIPOS_NF.RECEITA = 1)
   AND (TPEDIDOS_VENDA.TIPO = 'PDV')
   AND (( TCLIENTES.COD_CLI in (183,189,1881,392,2527,74,1257,67,1136,734,3258,1156,379,2429,864,859,1293,865,2584,223,2074,2858,60,1070,5,380,2344,60,38,36,179,382,1152,15,14,4,33,12,13,2502,911,3869,1897,3191,3)))